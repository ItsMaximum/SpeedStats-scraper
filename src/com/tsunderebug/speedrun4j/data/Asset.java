package com.tsunderebug.speedrun4j.data;

public class Asset {

	private String uri;
	private int width;
	private int height;

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public void setUri(String uri) {
		this.uri = uri;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public String getUri() {
		return uri;
	}

}
